// Variáveis - 3 formas
// var, let e const
// var não usa mais!

let nome = 'Daniel';

console.log(nome);
console.log('Daniel');

//reatribuição de valor na variável
nome = 'João'

console.log(nome);

//const não muda de valor

const idade = 30;

console.log(idade, nome);

//typeof mostra o tipo de dado
console.log(typeof nome);
console.log(typeof idade);

// boolean = true, false
let estaChovendo = false;
console.log(typeof estaChovendo);

console.log(10 > 5); //imprime true ou false

let semValor = null;

console.log(semValor);

// Tudo é objeto no JS
console.log(typeof semValor);

let indefinido;
console.log(indefinido);
console.log(typeof indefinido);

